    public static CultureInfo Culture(AppLanguage language) => language == AppLanguage.French
        ? CultureInfo.GetCultureInfo("fr-FR")
        : CultureInfo.GetCultureInfo("en-US");

    public static string Get(AppLanguage language, TextId id) => (language == AppLanguage.French ? French : English)[id];

    public static string Format(AppLanguage language, TextId id, params object?[] arguments) =>
        string.Format(Culture(language), Get(language, id), arguments);
